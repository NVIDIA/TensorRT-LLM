"""Unified legacy lint utilities: baseline-gated ruff + config generation.

Subcommands:

  lint-precommit FILE1.py FILE2.py ...
      Runs ruff --fix on the given files, detects auto-fixed modifications via
      file hashing, parses remaining violations from JSON output, and compares
      against the committed baseline. Fails on regressions (new violations
      exceeding the baseline count for a given file+rule pair) or when files
      are auto-modified by ruff --fix (to prompt re-staging).

  lint-update-violations
      Scans ALL legacy files and writes/overwrites ruff-legacy-baseline.json.
      Run this after batch cleanups or periodically to tighten the ratchet.

  gen-configs
      Regenerate ruff-legacy.toml and managed blocks in pyproject.toml /
      .pre-commit-config.yaml from legacy-files.txt (single source of truth).

  check-configs
      Verify generated configs are fresh (CI mode).

  prune-files
      Remove stale entries from legacy-files.txt.

Stdlib-only (no pip dependencies beyond ruff and jinja2).
"""

from __future__ import annotations

import argparse
import difflib
import hashlib
import json
import os
import subprocess
import sys
from collections import defaultdict
from pathlib import Path

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
REPO_ROOT = Path(__file__).resolve().parent.parent
LEGACY_FILES_TXT = REPO_ROOT / "legacy-files.txt"
BASELINE_PATH = REPO_ROOT / "ruff-legacy-baseline.json"
RUFF_CONFIG = REPO_ROOT / "ruff-legacy.toml"
RUFF_LEGACY_TOML = RUFF_CONFIG
PYPROJECT_TOML = REPO_ROOT / "pyproject.toml"
PRECOMMIT_YAML = REPO_ROOT / ".pre-commit-config.yaml"
TEMPLATES_DIR = REPO_ROOT / "scripts" / "templates"

# Maximum files per ruff invocation (avoids OS arg-length limits)
BATCH_SIZE = 500

# Marker strings used to delimit auto-generated blocks
BEGIN_MARKER = "# <<<< BEGIN AUTO-GENERATED by scripts/legacy_utils.py gen-configs >>>>"
END_MARKER = "# <<<< END AUTO-GENERATED >>>>"


# ---------------------------------------------------------------------------
# Exceptions
# ---------------------------------------------------------------------------


class RuffError(RuntimeError):
    """Raised when ruff fails unexpectedly or returns unparsable output."""


# ---------------------------------------------------------------------------
# Shared helpers
# ---------------------------------------------------------------------------


def eprint(*args, **kwargs):
    """Print to stderr (pre-commit convention)."""
    print(*args, file=sys.stderr, **kwargs)


def read_legacy_files() -> list[str]:
    """Read legacy-files.txt, return sorted non-empty non-comment lines.

    Raises FileNotFoundError if legacy-files.txt does not exist.
    """
    if not LEGACY_FILES_TXT.exists():
        raise FileNotFoundError(f"{LEGACY_FILES_TXT} not found")
    paths = []
    for line in LEGACY_FILES_TXT.read_text().splitlines():
        line = line.strip()
        if line and not line.startswith("#"):
            paths.append(line)
    return sorted(set(paths))


def hash_file(path: Path) -> str:
    """Return SHA-256 hex digest of a file's contents."""
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            h.update(chunk)
    return h.hexdigest()


def hash_files(paths: list[Path]) -> dict[Path, str]:
    """Return {path: sha256} for all given files."""
    return {p: hash_file(p) for p in paths if p.exists()}


def normalize_path(filepath: str, repo_root: Path) -> str:
    """Normalize an absolute path to repo-relative."""
    p = Path(filepath)
    if p.is_absolute():
        try:
            return str(p.relative_to(repo_root))
        except ValueError:
            return str(p)
    return str(p)


def count_violations(violations: list[dict], repo_root: Path) -> dict[str, dict[str, int]]:
    """Count violations per (file, rule) from ruff JSON output.

    Skips entries with code=null (syntax errors).
    Returns: {file_path: {rule_code: count}}
    """
    counts: dict[str, dict[str, int]] = defaultdict(lambda: defaultdict(int))
    for v in violations:
        code = v.get("code")
        if code is None:
            continue
        filepath = normalize_path(v.get("filename", ""), repo_root)
        counts[filepath][code] += 1
    return counts


def load_baseline() -> dict:
    """Load the baseline JSON file, stripping _meta key.

    Raises FileNotFoundError if the baseline file does not exist.
    """
    if not BASELINE_PATH.exists():
        try:
            display_path = str(BASELINE_PATH.relative_to(REPO_ROOT))
        except ValueError:
            display_path = str(BASELINE_PATH)
        raise FileNotFoundError(
            f"Baseline file not found: {display_path}\n"
            f"Run: python scripts/legacy_utils.py lint-update-violations"
        )
    with open(BASELINE_PATH) as f:
        data = json.load(f)
    data.pop("_meta", None)
    return data


def run_ruff_json(files: list[str], fix: bool = True) -> list[dict]:
    """Run ruff check --output-format json on the given files.

    When *fix* is True (default), ``--fix`` is included so ruff auto-corrects
    fixable violations. When False, violations are only counted.

    Returns the parsed JSON array of remaining violations.
    Raises RuffError on unexpected failures or unparsable output.
    """
    if not files:
        return []

    all_violations = []
    for i in range(0, len(files), BATCH_SIZE):
        batch = files[i : i + BATCH_SIZE]
        cmd = [
            "ruff",
            "check",
            "--config",
            str(RUFF_CONFIG),
        ]
        if fix:
            cmd.append("--fix")
        cmd += [
            "--exit-zero",
            "--output-format",
            "json",
        ] + batch

        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0 and not result.stdout.strip():
            raise RuffError(f"ruff failed unexpectedly:\n{result.stderr}")

        if result.stdout.strip():
            try:
                violations = json.loads(result.stdout)
                all_violations.extend(violations)
            except json.JSONDecodeError:
                raise RuffError(f"Could not parse ruff JSON output:\n{result.stdout[:500]}")

    return all_violations


def compare_against_baseline(
    current_counts: dict[str, dict[str, int]],
    baseline: dict[str, dict[str, int]],
    checked_files: set[str],
) -> tuple[list[tuple[str, str, int]], list[tuple[str, str, int]]]:
    """Compare current violation counts against baseline.

    Returns (regressions, improvements), each a list of (filepath, rule, delta).
    """
    regressions = []
    for filepath, rules in sorted(current_counts.items()):
        file_baseline = baseline.get(filepath, {})
        if isinstance(file_baseline, dict):
            for rule, count in sorted(rules.items()):
                baseline_count = file_baseline.get(rule, 0)
                if count > baseline_count:
                    regressions.append((filepath, rule, count - baseline_count))

    improvements = []
    for filepath in sorted(checked_files):
        file_baseline = baseline.get(filepath, {})
        if not isinstance(file_baseline, dict):
            continue
        file_current = current_counts.get(filepath, {})
        for rule, baseline_count in file_baseline.items():
            current_count = file_current.get(rule, 0)
            if current_count < baseline_count:
                improvements.append((filepath, rule, baseline_count - current_count))

    return regressions, improvements


def identify_new_violations(
    violations: list[dict],
    baseline: dict[str, dict[str, int]],
    repo_root: Path,
) -> list[tuple[str, int, str, str]]:
    """Identify individual violations that exceed the baseline.

    For each (file, rule) pair where current count > baseline count,
    includes ALL violations for that pair (we can't know which specific
    line is "new", so showing all is most actionable).

    Returns list of (filepath, line, code, message) sorted by filepath then line.
    Skips violations with code=None (syntax errors).
    """
    current_counts = count_violations(violations, repo_root)

    regressed_pairs: set[tuple[str, str]] = set()
    for filepath, rules in current_counts.items():
        file_baseline = baseline.get(filepath, {})
        if isinstance(file_baseline, dict):
            for rule, count in rules.items():
                if count > file_baseline.get(rule, 0):
                    regressed_pairs.add((filepath, rule))

    results: list[tuple[str, int, str, str]] = []
    for v in violations:
        code = v.get("code")
        if code is None:
            continue
        filepath = normalize_path(v.get("filename", ""), repo_root)
        if (filepath, code) in regressed_pairs:
            line = v.get("location", {}).get("row", 0)
            message = v.get("message", "")
            results.append((filepath, line, code, message))

    results.sort(key=lambda t: (t[0], t[1]))
    return results


def format_improvements(
    improvements: list[tuple[str, str, int]],
) -> list[str]:
    """Format improvement tuples as display lines.

    Returns list of "  filepath  rule (-delta)" strings, sorted by filepath then rule.
    """
    return [f"  {filepath}  {rule} (-{delta})" for filepath, rule, delta in sorted(improvements)]


