# Config generation helpers
# ---------------------------------------------------------------------------


def validate_paths(paths: list[str]) -> list[str]:
    """Warn about paths that don't exist on disk. Return list of stale paths."""
    stale = []
    for p in paths:
        if not (REPO_ROOT / p).exists():
            stale.append(p)
    if stale:
        eprint(f"WARNING: {len(stale)} stale entries in legacy-files.txt:")
        for s in stale:
            eprint(f"  {s}")
    return stale


def render_template(template_name: str, paths: list[str]) -> str:
    """Render a Jinja2 template with the given paths list."""
    from jinja2 import Environment, FileSystemLoader

    env = Environment(
        loader=FileSystemLoader(str(TEMPLATES_DIR)),
        keep_trailing_newline=True,
    )
    template = env.get_template(template_name)
    return template.render(paths=paths)


def replace_managed_block(content: str, new_block: str) -> str:
    """Replace content between BEGIN/END markers with new_block.

    The new_block should include the BEGIN and END markers themselves.
    """
    begin_idx = content.find(BEGIN_MARKER)
    end_idx = content.find(END_MARKER, begin_idx + len(BEGIN_MARKER)) if begin_idx != -1 else -1

    if begin_idx == -1 or end_idx == -1:
        raise ValueError(
            f"Could not find managed block markers:\n  BEGIN: {BEGIN_MARKER}\n  END:   {END_MARKER}"
        )

    nl = content.find("\n", end_idx)
    end_line_end = (nl + 1) if nl != -1 else len(content)

    return content[:begin_idx] + new_block + content[end_line_end:]


def show_diff(label: str, old: str, new: str) -> bool:
    """Print unified diff if different. Return True if they differ."""
    if old == new:
        return False
    diff = difflib.unified_diff(
        old.splitlines(keepends=True),
        new.splitlines(keepends=True),
        fromfile=f"a/{label}",
        tofile=f"b/{label}",
    )
    sys.stdout.writelines(diff)
    return True


def generate_ruff_legacy_toml(paths: list[str]) -> str:
    """Generate the full ruff-legacy.toml content."""
    return render_template("ruff-legacy.toml.j2", paths)


def generate_pyproject_block(paths: list[str]) -> str:
    """Generate the managed block for pyproject.toml [tool.ruff.format] exclude."""
    return render_template("pyproject-format-exclude.j2", paths)


def generate_precommit_block(paths: list[str]) -> str:
    """Generate the managed block for .pre-commit-config.yaml file anchors."""
    return render_template("precommit-legacy-files.j2", paths)


# ---------------------------------------------------------------------------
# Config generation actions
# ---------------------------------------------------------------------------


def do_generate(paths: list[str]) -> None:
    """Write all generated files in-place.

    Computes all outputs first so that if any marker is missing,
    no files are written (atomic with respect to validation).
    """
    new_toml = generate_ruff_legacy_toml(paths)

    old_pyproject = PYPROJECT_TOML.read_text()
    new_pyproject_block = generate_pyproject_block(paths)
    new_pyproject = replace_managed_block(old_pyproject, new_pyproject_block)

    old_precommit = PRECOMMIT_YAML.read_text()
    new_precommit_block = generate_precommit_block(paths)
    new_precommit = replace_managed_block(old_precommit, new_precommit_block)

    RUFF_LEGACY_TOML.write_text(new_toml)
    print(f"  Written: {RUFF_LEGACY_TOML.relative_to(REPO_ROOT)}")

    PYPROJECT_TOML.write_text(new_pyproject)
    print(f"  Updated: {PYPROJECT_TOML.relative_to(REPO_ROOT)}")

    PRECOMMIT_YAML.write_text(new_precommit)
    print(f"  Updated: {PRECOMMIT_YAML.relative_to(REPO_ROOT)}")


def do_check(paths: list[str]) -> None:
    """Check freshness -- exit 1 with diff if any file is stale."""
    any_stale = False

    expected = generate_ruff_legacy_toml(paths)
    actual = RUFF_LEGACY_TOML.read_text() if RUFF_LEGACY_TOML.exists() else ""
    if show_diff("ruff-legacy.toml", actual, expected):
        any_stale = True

    old_pyproject = PYPROJECT_TOML.read_text()
    new_block = generate_pyproject_block(paths)
    expected_pyproject = replace_managed_block(old_pyproject, new_block)
    if show_diff("pyproject.toml", old_pyproject, expected_pyproject):
        any_stale = True

    old_precommit = PRECOMMIT_YAML.read_text()
    new_block = generate_precommit_block(paths)
    expected_precommit = replace_managed_block(old_precommit, new_block)
    if show_diff(".pre-commit-config.yaml", old_precommit, expected_precommit):
        any_stale = True

    if any_stale:
        eprint(
            "\nERROR: Generated configs are out of date. Run:\n"
            "  python scripts/legacy_utils.py gen-configs\n"
        )
        sys.exit(1)
    else:
        print("All generated configs are up to date.")


def do_prune() -> None:
    """Remove stale paths from legacy-files.txt."""
    paths = read_legacy_files()
    stale = validate_paths(paths)
    if not stale:
        print("No stale entries found.")
        return
    valid = [p for p in paths if p not in set(stale)]
    LEGACY_FILES_TXT.write_text("\n".join(valid) + "\n")
    print(f"Pruned {len(stale)} stale entries. {len(valid)} entries remain.")


# ---------------------------------------------------------------------------
# Pre-commit mode
# ---------------------------------------------------------------------------


def precommit_mode(file_args: list[str]) -> int:
    """Run the pre-commit check. Returns exit code."""
    repo_root = REPO_ROOT
    baseline = load_baseline()

    abs_paths = []
    for f in file_args:
        p = Path(f)
        if not p.is_absolute():
            p = Path.cwd() / p
        p = p.resolve()
        abs_paths.append(p)

    hashes_before = hash_files(abs_paths)

    str_paths = [str(p) for p in abs_paths]
    violations = run_ruff_json(str_paths)

    hashes_after = hash_files(abs_paths)

    modified_files = []
    for p in abs_paths:
        before = hashes_before.get(p)
        after = hashes_after.get(p)
        if before and after and before != after:
            modified_files.append(normalize_path(str(p), repo_root))

    current_counts = count_violations(violations, repo_root)

    checked_files = {normalize_path(str(p), repo_root) for p in abs_paths}
    regressions, improvements = compare_against_baseline(current_counts, baseline, checked_files)

    has_failure = bool(modified_files) or bool(regressions)

    if modified_files:
        eprint(f"\nAuto-fixed {len(modified_files)} file(s):")
        for f in sorted(modified_files):
            eprint(f"  {f}")

    if regressions:
        new_violations = identify_new_violations(violations, baseline, repo_root)
        eprint(f"\nNew violations ({len(new_violations)} regressions vs baseline):\n")
        for filepath, line, code, message in new_violations:
            eprint(f"  {filepath}:{line}  {code}  {message}")
        eprint("\nTo inspect: ruff check --config ruff-legacy.toml <file>")

    if has_failure:
        eprint("\nRe-stage modified files and fix new violations, then commit again.")
        return 1

    if improvements:
        total = sum(delta for _, _, delta in improvements)
        n_files = len({f for f, _, _ in improvements})
        eprint(f"\nTip: {total} violation(s) resolved in {n_files} file(s). Run:")
        eprint("  python scripts/legacy_utils.py lint-update-violations")
        eprint("to tighten the baseline.\n")
        for line in format_improvements(improvements):
            eprint(line)

    return 0


# ---------------------------------------------------------------------------
# Update baseline mode
# ---------------------------------------------------------------------------


def update_baseline_mode() -> int:
    """Scan all legacy files and write the baseline JSON. Returns exit code."""
    repo_root = REPO_ROOT

    legacy_paths = read_legacy_files()
    existing = [p for p in legacy_paths if (repo_root / p).exists()]
    eprint(
        f"Scanning {len(existing)} legacy files ({len(legacy_paths) - len(existing)} missing)..."
    )

    violations = run_ruff_json(existing, fix=False)
    counts = count_violations(violations, repo_root)

    total_violations = 0
    total_files = 0
    baseline = {}
    for filepath in sorted(counts.keys()):
        rules = counts[filepath]
        baseline[filepath] = dict(sorted(rules.items()))
        total_violations += sum(rules.values())
        total_files += 1

    output = {
        "_meta": {
            "generated_by": "scripts/legacy_utils.py lint-update-violations",
            "total_violations": total_violations,
            "total_files": total_files,
        },
    }
    output.update(baseline)

    with open(BASELINE_PATH, "w") as f:
        json.dump(output, f, indent=2)
        f.write("\n")

    eprint(f"Baseline written: {BASELINE_PATH.relative_to(repo_root)}")
    eprint(f"  {total_violations} violations across {total_files} files")
    return 0


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def _build_parser() -> argparse.ArgumentParser:
    """Build the CLI argument parser with subcommands."""
    parser = argparse.ArgumentParser(
        description="Unified legacy lint utilities: baseline-gated ruff + config generation."
    )
    subparsers = parser.add_subparsers(dest="command")

    # lint-precommit
    sp_precommit = subparsers.add_parser(
        "lint-precommit",
        help="Run baseline-gated ruff lint on given files (pre-commit hook)",
    )
    sp_precommit.add_argument("files", nargs="+", help="Files to check")

    # lint-update-violations
    subparsers.add_parser(
        "lint-update-violations",
        help="Scan ALL legacy files and write/overwrite ruff-legacy-baseline.json",
    )

    # gen-configs
    subparsers.add_parser(
        "gen-configs",
        help="Regenerate ruff-legacy.toml and managed blocks from legacy-files.txt",
    )

    # check-configs
    subparsers.add_parser(
        "check-configs",
        help="Verify generated configs are up to date (CI mode)",
    )

    # prune-files
    subparsers.add_parser(
        "prune-files",
        help="Remove stale paths from legacy-files.txt",
    )

    return parser


def main() -> int:
    parser = _build_parser()
    args = parser.parse_args()

    try:
        if args.command == "lint-precommit":
            return precommit_mode(args.files)

        if args.command == "lint-update-violations":
            return update_baseline_mode()

        if args.command in ("gen-configs", "check-configs", "prune-files"):
            os.chdir(REPO_ROOT)

        if args.command == "prune-files":
            do_prune()
            return 0

        if args.command in ("gen-configs", "check-configs"):
            paths = read_legacy_files()
            stale = validate_paths(paths)

            if stale and args.command == "gen-configs":
                eprint(
                    "HINT: Run 'python scripts/legacy_utils.py prune-files' to remove stale entries."
                )

            if args.command == "check-configs":
                do_check(paths)
            else:
                do_generate(paths)
            return 0

        # No subcommand
        parser.print_help(sys.stderr)
        return 1

    except FileNotFoundError as e:
        eprint(f"ERROR: {e}")
        return 1
    except RuffError as e:
        eprint(f"ERROR: {e}")
        return 1


if __name__ == "__main__":
    sys.exit(main())
